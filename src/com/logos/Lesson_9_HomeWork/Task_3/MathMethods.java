package com.logos.Lesson_9_HomeWork.Task_3;

public class MathMethods implements Methods{
    public static double suma(double number_1, double number_2){
        return number_1 + number_2;
    }

    public static double vidnimanya(double number_1, double number_2){
        return number_1 - number_2;
    }

    public static double mnojenya(double number_1, double number_2){
        return number_1 * number_2;
    }

    public static double dilenya(double number_1, double number_2){
        return number_1 / number_2;
    }
}
